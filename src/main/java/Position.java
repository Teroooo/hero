public class Position {
    private int x;
    private int y;


    public Position(int i, int i1) {
        x=i;
        y=i1;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
